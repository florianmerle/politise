	    	<b>Avec plus de 53 % des suffrages, le candidat libéral devance largement son adversaire le nationaliste de droite Norbert Hofer. Mais les politiciens au pouvoir vont-ils comprendre le message ?</b><br/><br/>

	    	Fortement mobilisés, les électeurs autrichiens ont confirmé ce dimanche 4 décembre leur choix du 22 mai en faveur du candidat libéral Alexander Van der Bellen.<br/><br/>

	    	<i>"Van der Bellen ne sera pas un mauvais président, commente avec une certaine amertume Die Presse, il marchera sur les traces du [précédent président social-démocrate] Heinz Fischer”. “Écoute, mise en garde, médiation, plaidoyer pour la bonne cause en coulisse…"</i>Le quotidien conservateur égrène ses qualités avant de se lancer dans cette métaphore : les années à venir nous offrent <i>"un wagon-lit, donc, et pas un grand huit"</i>. Autant dire la tourmente qu’eut été un président Hofer.<br/><br/>

	    	C’est <i>"le vote de l’autre Autriche"</i> qui s’est imposé, salue de son côté le quotidien de centre gauche Der Standard. Et ce ne sont <i>pas seulement les élites</i> qui ont élu Van der Bellen, mais tous ceux qui ne voulaient pas d’un populiste de droite, nationaliste, à la présidence. L’orientation politique du pays était en jeu, l’ancrage européen de l’Autriche, les Autrichiens ont fait leur choix, même si ce fut parfois moins par enthousiasme que pour privilégier <i>"le moindre mal"</i>.<br/><br/>

	    	<b>Quelles leçons tirer de cette campagne ?</b><br/><br/>

	    	<i>"La vieille République a encore une fois réussi à mobiliser toutes ses ressources pour barrer la route au FPÖ"</i>, mais on aurait tort de croire que c’est <i>"une victoire du système"</i>. La majorité des Autrichiens n’a pas voulu d’un <i>"mini-Trump"</i> à la présidence, mais ils ont donné un signal d’alarme que les partis au pouvoir (grande coalition SPÖ-ÖVP) auraient tort d’ignorer, analyse Die Presse.<br/><br/>

	    	Même inquiétude dans les colonnes du quotidien de centre droit Kurier, pour lequel les partis au gouvernement n’ont pas l’air d’avoir tiré beaucoup d’enseignements de cette campagne - à laquelle ils ne participaient pas compte tenu de leur échec au premier tour :<br/><br/>

	    	<i>"Nous allons bientôt savoir si les hauts dirigeants des deux partis ont compris que des élections anticipées n’apportent rien et que les électeurs sont fatigués des tours de scrutin. Ce qu’ils veulent, ce sont des décisions."</i><br/><br/>

	    	Le président Van der Bellen prendra officiellement ses fonctions le 26 janvier 2017 pour un mandat de six ans.
